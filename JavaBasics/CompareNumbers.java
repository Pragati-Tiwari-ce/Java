public class CompareNumbers {
    public static void main(String[] args) {
        int a = 3;
        int b = 3;

        if (a > b) {
            System.out.println("A is greater");
        } else if (a < b) {
            System.out.println("B is greater");
        } else {
            System.out.println("Both are equal");
        }
    }
}
